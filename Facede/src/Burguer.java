public class Burguer extends Produto{
    private int gramas;

    public int getGramas() {
        return gramas;
    }

    public  Burguer(int gramas, String descricao, double preco) {
        super(descricao, preco);
        this.gramas = gramas;
    }

    @Override
    public void MostrarBurguer() {
        return MostrarBurguer();
    }
}
